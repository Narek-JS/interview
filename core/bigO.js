/**********************************************/
/*********** What is Big O notation ***********/
/**********************************************/

/*
    Big O notation is a mathematical notation used in computer science to describe the asymptotic behavior or performance of an algorithm. 
    It provides a way to analyze and compare the efficiency of algorithms by expressing how the runtime or space requirements of an algorithm grow relative to the input size. 
    In Big O notation, we focus on the worst-case scenario or the upper bound of the algorithm's performance.
*/

/**************************************************************************************/

/***************************************/
/*********** Time Complexity ***********/
/***************************************/

/*
    Time complexity refers to the amount of time or number of operations required by an algorithm to run as a function of the input size. 
    It is commonly expressed using Big O notation. Time complexity analysis helps us understand how the algorithm's performance scales with the input size. 
    For example, an algorithm with a time complexity of O(n) means that the runtime of the algorithm grows linearly with the input size (e.g., if the input size doubles, the runtime doubles as well).
*/

/**************************************************************************************/

/****************************************/
/*********** Space Complexity ***********/
/****************************************/

/*
    Space complexity refers to the amount of memory or space required by an algorithm to run as a function of the input size. 
    Similar to time complexity, space complexity is also expressed using Big O notation. 
    Space complexity analysis helps us understand how the algorithm's memory usage scales with the input size. 
    For example, an algorithm with a space complexity of O(n) means that the algorithm's memory usage grows linearly with the input size.
*/
